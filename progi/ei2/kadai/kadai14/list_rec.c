//------------------------------------------------------------------------
int insertUpOrder(List **head, int data) {
	// 対象のセルが空、もしくは、対象のセルのデータが新しいデータより大きい場合
	if (isEmptyList(*head) == 1 || getCellData(*head) >= data) {
		return (insertHead(head, data));
	}
}

