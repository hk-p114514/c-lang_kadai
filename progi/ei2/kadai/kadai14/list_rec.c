//------------------------------------------------------------------------
// 問32(1) リスト内のデータを先頭から順に表示する（問27(1)⑩ 再帰版）
//  ［引　数］head : リストの先頭セルへのポインタ（リストの先頭アドレス）
//                  （注：リストの先頭セルは変更されない）
//  ［戻り値］なし
//------------------------------------------------------------------------
void printList(List *head) {
	// headをコピー
	Cell *cell = head;
	if (isEmptyList(cell) != 1) {
		// 最後のセルなら終了
		return;
	} else {
		// cell(head)の次のセルを取得
		cell = getNextCell(cell);
		int data = getCellData(cell);

		printf("%d ", data);

		// 次のセルを取得
		cell = getNextCell(cell);
		if (isEmptyList(cell) != 1) {
			// 次のセルが存在するので矢印表示
			printf("-> ");
		}
		printList(cell);
	}
}

