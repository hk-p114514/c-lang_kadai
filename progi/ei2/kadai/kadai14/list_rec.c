// 問27(2-a) 次のセルのポインタを保持しているメンバ変数のアドレスを取得する
//  ［引　数］cell : 操作対象となるセルへのポインタ
//  ［戻り値］次のセルへのポインタを保持しているメンバ変数を指すポインタ
